create or replace function public.set_announcement_audit_fields()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_active_admin() then
    raise exception 'Only active admins can manage announcements';
  end if;

  if tg_op = 'INSERT' then
    new.created_by := auth.uid();
    new.created_at := coalesce(new.created_at, now());
    new.updated_at := now();
  elsif tg_op = 'UPDATE' then
    new.created_by := old.created_by;
    new.created_at := old.created_at;
    new.updated_at := now();
  end if;

  return new;
end;
$$;

revoke all on function public.set_announcement_audit_fields() from public, anon;

drop trigger if exists set_announcement_audit_fields on public.announcements;
create trigger set_announcement_audit_fields
before insert or update on public.announcements
for each row execute function public.set_announcement_audit_fields();

