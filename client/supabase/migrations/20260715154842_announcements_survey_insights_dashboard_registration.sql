create or replace function public.get_admin_survey_response_details(p_survey_id integer default null, p_search text default null)
returns table (
  response_id integer,
  survey_id integer,
  survey_title text,
  profile_id uuid,
  fullname text,
  email text,
  purok text,
  gender text,
  submitted_at timestamptz,
  answers jsonb
)
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_active_admin() then
    raise exception 'Only active admins can view identified survey responses';
  end if;

  return query
  select
    sr.response_id,
    sr.survey_id,
    s.title::text as survey_title,
    kp.profile_id,
    kp.fullname::text,
    kp.email::text,
    kp.purok::text,
    kp.gender::text,
    sr.submitted_at,
    coalesce(jsonb_agg(jsonb_build_object(
      'question_id', sq.question_id,
      'question_text', sq.question_text,
      'question_type', sq.question_type,
      'answer_text', sa.answer_text,
      'selected_options', coalesce(opts.options, '[]'::jsonb)
    ) order by sq.sort_order, sq.question_id) filter (where sa.answer_id is not null), '[]'::jsonb) as answers
  from public.survey_responses sr
  join public.surveys s on s.survey_id = sr.survey_id
  join public.kabataan_profiles kp on kp.profile_id = sr.user_id
  left join public.survey_answers sa on sa.response_id = sr.response_id
  left join public.survey_questions sq on sq.question_id = sa.question_id
  left join lateral (
    select jsonb_agg(jsonb_build_object('option_id', so.option_id, 'option_text', so.option_text) order by so.sort_order, so.option_id) as options
    from public.survey_answer_options sao
    join public.survey_options so on so.option_id = sao.option_id
    where sao.answer_id = sa.answer_id
  ) opts on true
  where (p_survey_id is null or sr.survey_id = p_survey_id)
    and (p_search is null or trim(p_search) = '' or kp.fullname ilike '%' || trim(p_search) || '%' or kp.email ilike '%' || trim(p_search) || '%')
  group by sr.response_id, sr.survey_id, s.title, kp.profile_id, kp.fullname, kp.email, kp.purok, kp.gender, sr.submitted_at
  order by sr.submitted_at desc;
end;
$$;

revoke all on function public.get_admin_survey_response_details(integer, text) from public, anon;
grant execute on function public.get_admin_survey_response_details(integer, text) to authenticated;

create or replace function public.get_my_survey_responses()
returns table (response_id integer, survey_id integer, survey_title text, submitted_at timestamptz, answers jsonb)
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.uid() is null then
    raise exception 'Authentication required';
  end if;

  return query
  select sr.response_id, sr.survey_id, s.title::text, sr.submitted_at,
    coalesce(jsonb_agg(jsonb_build_object(
      'question_id', sq.question_id,
      'question_text', sq.question_text,
      'question_type', sq.question_type,
      'answer_text', sa.answer_text,
      'selected_options', coalesce(opts.options, '[]'::jsonb)
    ) order by sq.sort_order, sq.question_id) filter (where sa.answer_id is not null), '[]'::jsonb) as answers
  from public.survey_responses sr
  join public.surveys s on s.survey_id = sr.survey_id
  left join public.survey_answers sa on sa.response_id = sr.response_id
  left join public.survey_questions sq on sq.question_id = sa.question_id
  left join lateral (
    select jsonb_agg(jsonb_build_object('option_id', so.option_id, 'option_text', so.option_text) order by so.sort_order, so.option_id) as options
    from public.survey_answer_options sao
    join public.survey_options so on so.option_id = sao.option_id
    where sao.answer_id = sa.answer_id
  ) opts on true
  where sr.user_id = auth.uid()
  group by sr.response_id, sr.survey_id, s.title, sr.submitted_at
  order by sr.submitted_at desc;
end;
$$;

revoke all on function public.get_my_survey_responses() from public, anon;
grant execute on function public.get_my_survey_responses() to authenticated;

create or replace view public.survey_preferred_activity_types
with (security_invoker = true)
as
with relevant_answers as (
  select distinct sr.response_id, sr.user_id, s.survey_id, s.title as source_survey, sq.question_id, so.option_id, so.option_text as activity_type
  from public.survey_responses sr
  join public.surveys s on s.survey_id = sr.survey_id
  join public.survey_answers sa on sa.response_id = sr.response_id
  join public.survey_questions sq on sq.question_id = sa.question_id
  join public.survey_answer_options sao on sao.answer_id = sa.answer_id
  join public.survey_options so on so.option_id = sao.option_id
  where s.status in ('published', 'closed')
    and sq.question_type in ('single_choice', 'multiple_choice')
    and (sq.reporting_key = 'preferred_activity_type' or lower(sq.question_text) like any (array['%preferred activity%', '%activity type%', '%program type%', '%interest%']))
),
option_counts as (
  select activity_type, count(*)::int as selection_count, count(distinct user_id)::int as respondent_count, array_agg(distinct source_survey order by source_survey) as source_surveys
  from relevant_answers
  group by activity_type
),
totals as (
  select count(*)::numeric as total_selections, count(distinct user_id)::numeric as total_respondents
  from relevant_answers
)
select
  row_number() over (order by respondent_count desc, selection_count desc, activity_type) as rank,
  activity_type,
  respondent_count,
  total_respondents::int,
  case when total_respondents > 0 then round((respondent_count::numeric / total_respondents) * 100, 2) else 0 end as respondent_percentage,
  selection_count,
  total_selections::int,
  case when total_selections > 0 then round((selection_count::numeric / total_selections) * 100, 2) else 0 end as selection_share_percentage,
  source_surveys
from option_counts, totals;

revoke all on public.survey_preferred_activity_types from public, anon, authenticated;

create or replace view public.survey_top_suggested_events
with (security_invoker = true)
as
with raw_suggestions as (
  select distinct sr.response_id, sr.user_id, s.title as source_survey, lower(trim(so.option_text)) as normalized_name, trim(so.option_text) as display_name, null::text as category
  from public.survey_responses sr
  join public.surveys s on s.survey_id = sr.survey_id
  join public.survey_answers sa on sa.response_id = sr.response_id
  join public.survey_questions sq on sq.question_id = sa.question_id
  join public.survey_answer_options sao on sao.answer_id = sa.answer_id
  join public.survey_options so on so.option_id = sao.option_id
  where s.status in ('published', 'closed')
    and nullif(trim(so.option_text), '') is not null
    and (sq.reporting_key = 'suggested_event' or lower(sq.question_text) like any (array['%suggested event%', '%event suggestion%', '%activity suggestion%']))
  union all
  select distinct sr.response_id, sr.user_id, s.title as source_survey, lower(trim(sa.answer_text)) as normalized_name, trim(sa.answer_text) as display_name, null::text as category
  from public.survey_responses sr
  join public.surveys s on s.survey_id = sr.survey_id
  join public.survey_answers sa on sa.response_id = sr.response_id
  join public.survey_questions sq on sq.question_id = sa.question_id
  where s.status in ('published', 'closed')
    and sq.question_type in ('short_text', 'long_text')
    and nullif(trim(sa.answer_text), '') is not null
    and (sq.reporting_key = 'suggested_event' or lower(sq.question_text) like any (array['%suggested event%', '%event suggestion%', '%activity suggestion%']))
),
deduped_support as (
  select distinct user_id, normalized_name
  from raw_suggestions
  where normalized_name <> ''
),
grouped as (
  select
    r.normalized_name,
    initcap(min(r.display_name)) as suggested_event_name,
    coalesce(max(r.category), 'Uncategorized') as category,
    count(*)::int as submission_count,
    count(distinct r.user_id)::int as respondent_count,
    array_agg(distinct r.source_survey order by r.source_survey) as source_surveys
  from raw_suggestions r
  where r.normalized_name <> ''
  group by r.normalized_name
),
total as (
  select count(*)::numeric as total_distinct_support
  from deduped_support
),
planned as (
  select event_id, lower(trim(event_name)) as normalized_name
  from public.events
  where status in ('scheduled', 'ongoing')
)
select
  row_number() over (order by g.respondent_count desc, g.submission_count desc, g.suggested_event_name) as rank,
  g.suggested_event_name,
  g.category,
  g.respondent_count,
  g.submission_count,
  case when total.total_distinct_support > 0 then round((g.respondent_count::numeric / total.total_distinct_support) * 100, 2) else 0 end as respondent_support_percentage,
  g.source_surveys,
  (p.event_id is not null) as is_already_planned,
  p.event_id as matching_event_id
from grouped g
cross join total
left join planned p on p.normalized_name = g.normalized_name;

revoke all on public.survey_top_suggested_events from public, anon, authenticated;

